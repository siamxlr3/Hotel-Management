export const useTranslate = () => {
  const { language } = useSelector(s => s.locale);
  return (key) => translate(key, language);
};

export const translate = (key, lang = 'ENG') => {
  if (!key) return '';
  const k = String(key).toLowerCase();
  if (dictionary[lang] && dictionary[lang][k]) {
    return dictionary[lang][k];
  }
  if (dictionary['ENG'] && dictionary['ENG'][k]) {
    return dictionary['ENG'][k];
  }
  return key;
};

export const translateDigits = (num, lang = 'ENG') => {
  if (lang !== 'BAN') return num;
  const digitsBAN = { '0': '০', '1': '১', '2': '২', '3': '৩', '4': '৪', '5': '৫', '6': '৬', '7': '৭', '8': '৮', '9': '৯' };
  return String(num || '').split('').map(char => digitsBAN[char] || char).join('');
};

export const getCurrencySymbol = (currency = 'USD') => {
  return currency === 'BDT' ? '৳' : '$';
};

export const formatPrice = (amount, currency = 'USD', lang = 'ENG') => {
  const num = Number(amount || 0).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  const formatted = `${getCurrencySymbol(currency)}${num}`;
  return translateDigits(formatted, lang);
};
